package all.java.java360.operadores;

/**
 * Introdução a operadores, operadores matemáticos
 */
public class Operators {
    public static void main(String[] args) {
        int value1 = 10;
        int value2 = 20;

        System.out.println("sum: " + (value1 + value2));
        System.out.println("sub: " + (value2 - value1));
        System.out.println("mult: " + (value1 * value2));
        System.out.println("div: " + (value2 / value1));
        System.out.println("module: " + (value1 % value2));
    }
}
